/* Includes ------------------------------------------------------------------*/
#include "buzzer.h"
#include "timer.h"
#include "delay.h"

/*******************************************************************************
* @brief    --> 蜂鸣器的定时器通道初始化
* @param    --> None
* @retval   --> None
*******************************************************************************/
void Buzzer_Init(void)
{
  TIM3_PWM_Init(28799, 0);	 //不分频. PWM频率=72000000/28000=2500Hz
}

/*******************************************************************************
* @brief    --> 嘀嘀嘀
* @param    --> None
* @retval   --> None
*******************************************************************************/
void Buzzer_Sound1(void)
{
  TIM_SetCompare1(TIM3, 5000);
  delay_ms(150);
  TIM_SetCompare1(TIM3, 0);
  delay_ms(100);
  TIM_SetCompare1(TIM3, 5000);
  delay_ms(150);
  TIM_SetCompare1(TIM3, 0);
  delay_ms(100);
  TIM_SetCompare1(TIM3, 5000);
  delay_ms(150);
  TIM_SetCompare1(TIM3, 0);
}
